package edu.uic.cs.dbis.jtv.misc;

public class TVerifierException extends RuntimeException
{
	private static final long serialVersionUID = 1L;

	public TVerifierException()
	{
		super();
	}

	public TVerifierException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public TVerifierException(String message)
	{
		super(message);
	}

	public TVerifierException(Throwable cause)
	{
		super(cause);
	}

}
